package com.guvi.product;

public class Product {

	// Initializing variables
	int pid;
	int price;
	int quantity;

	// Zero Parameter constructor
	Product() {

	}

	// Parameterized Constructor
	Product(int pid, int price, int quantity) {
		this.pid = pid;
		this.price = price;
		this.quantity = quantity;
	}

}
